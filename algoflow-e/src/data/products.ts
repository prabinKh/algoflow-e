export const products = generateProducts();

export const getProductBySlug = (slug: string) => products.find(p => p.slug === slug);

export const getProductsByCategory = (categorySlug: string) =>
  products.filter(p => p.categorySlug === categorySlug);

export const getNewArrivals = () => products.filter(p => p.isNew);

export const getBestPrice = () => products.filter(p => p.discount && p.discount > 0).sort((a, b) => (b.discount || 0) - (a.discount || 0));

export const getPopular = () => [...products].sort((a, b) => b.reviews - a.reviews).slice(0, 8);

export const getDealOfTheWeek = () => products.filter(p => p.discount && p.discount >= 10).slice(0, 6);

export const formatPrice = (price: number) => `Rs.${price.toLocaleString('en-NP')}`;
